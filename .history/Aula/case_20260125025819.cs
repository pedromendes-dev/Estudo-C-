// Case é padrão de escrita usado para nomear classes, métodos, variáveis, etc.
// Exemplos de case: camelCase, PascalCase, snake_case, kebab-case

// camelCase: A primeira letra da primeira palavra é minúscula e a primeira letra de cada palavra subsequente é maiúscula. Ex: minhaVariavelImportante

int minhaVariavelImportante = 10;

// PascalCase: A primeira letra de cada palavra é maiúscula. Ex: MinhaClasseImportante

int MinhaClasseImportante = 20;

// snake_case: Todas as letras são minúsculas e as palavras são separadas por underscores (_). Ex: minha_variavel_importante

int minha_variavel_importante = 30;

// spinal-case (kebab-case): Todas as letras são minúsculas e as palavras são separadas por hífens (-). Ex: minha-variavel-importante

minha_variavel_importante